#include<iostream>

using namespace std;

int main(){
    int a = 10, b = 20;
    cout << "Addition: " << a + b << endl; // Addition
    cout << "Subtraction: " << a - b << endl; // Subtraction
    cout << "Multiplication: " << a * b << endl; // Multiplication
    cout << "Division: " << a / b << endl; // Division
    cout << "Modulus: " << a % b << endl; // Modulus

    return 0;
}