#include<iostream>

using namespace std ; 

int main(){
    int a = 20 , b = 30  ; 
    cout << "the Addition of a & b : "<<  a + b <<endl; // ADDITION 
    cout << "the subtraction of a & b : "<< a - b << endl ; // SUBTRACTION 
    cout << "the multiplication of a & b : " << a * b << endl; // MULTIPLICATION 
    cout << "the division of a & b : " << a / b  << endl; // DIVISION 
    cout << "the modulo of a & b : "<< a % b << endl ; // MODULO 
    return 0 ; 
}